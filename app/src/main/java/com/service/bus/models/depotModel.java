package com.service.bus.models;



public class depotModel {

    String depotName;
    String depotAddress;
    String depotEmailId;
    String depotContactNo;
    String depotProof;

    public String getDepotName() {
        return depotName;
    }

    public void setDepotName(String depotName) {
        this.depotName = depotName;
    }

    public String getDepotAddress() {
        return depotAddress;
    }

    public void setDepotAddress(String depotAddress) {
        this.depotAddress = depotAddress;
    }

    public String getDepotEmailId() {
        return depotEmailId;
    }

    public void setDepotEmailId(String depotEmailId) {
        this.depotEmailId = depotEmailId;
    }

    public String getDepotContactNo() {
        return depotContactNo;
    }

    public void setDepotContactNo(String depotContactNo) {
        this.depotContactNo = depotContactNo;
    }

    public String getDepotProof() {
        return depotProof;
    }

    public void setDepotProof(String depotProof) {
        this.depotProof = depotProof;
    }

}
